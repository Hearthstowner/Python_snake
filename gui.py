    def create_image(self):
        """Создает изображение на основании списка"""
        screen1 = pygame.Surface((441, 441), pygame.SRCALPHA, 32)
        x = 1
        y = 1
        for i in self.level:
            if i == 0:
                pygame.draw.rect(screen1, pygame.Color("Gray"), pygame.Rect(x, y, 10, 10))
            x += 11
            if x == 441:
                y += 11
                x = 1
        pygame.image.save(screen1, "images/gui.png")

    def draw_level(self, screen):
        """Отрисовывает уровень игры"""
        screen.blit(self.gui_image, (0, 0))